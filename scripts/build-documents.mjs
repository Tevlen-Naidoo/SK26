// Scans public/documents/** and writes src/data/documents.generated.ts.
// Just dump files into public/documents/ and re-run `npm run documents` —
// the Documents page picks them up automatically.
import { readdirSync, statSync, writeFileSync, existsSync } from 'node:fs';
import { join, relative, extname, basename, sep } from 'node:path';

const ROOT = 'public/documents';
const OUT = 'src/data/documents.generated.ts';
const IGNORE = new Set(['manifest.json', 'README.md', '.gitkeep', '.DS_Store']);
const IMAGE = new Set([
	'.png', '.jpg', '.jpeg', '.gif', '.webp', '.avif', '.heic', '.svg', '.bmp', '.tif', '.tiff',
]);

/** @param {string} dir */
function walk(dir) {
	const out = [];
	if (!existsSync(dir)) return out;
	for (const name of readdirSync(dir)) {
		if (name.startsWith('.') || IGNORE.has(name)) continue;
		const full = join(dir, name);
		const st = statSync(full);
		if (st.isDirectory()) out.push(...walk(full));
		else out.push({ full, size: st.size });
	}
	return out;
}

/** @param {string} file */
function niceName(file) {
	return basename(file, extname(file)).replace(/[_-]+/g, ' ').replace(/\s+/g, ' ').trim();
}

/** @param {string} ext */
function kindOf(ext) {
	if (ext === '.pdf') return 'pdf';
	if (IMAGE.has(ext)) return 'image';
	return 'other';
}

const files = walk(ROOT).sort((a, b) => a.full.localeCompare(b.full));

const items = files.map(({ full, size }) => {
	const rel = relative(ROOT, full).split(sep).join('/');
	const ext = extname(full).toLowerCase();
	return {
		id: rel,
		name: niceName(full),
		url: '/documents/' + rel.split('/').map(encodeURIComponent).join('/'),
		filename: basename(full),
		ext,
		kind: kindOf(ext),
		size,
	};
});

const body = `// AUTO-GENERATED by scripts/build-documents.mjs — do not edit by hand.
// Drop files into public/documents/ and re-run \`npm run documents\`.
import type { DocItem } from '../types.js';

export const DOCUMENTS: DocItem[] = ${JSON.stringify(items, null, '\t')};
`;

writeFileSync(OUT, body);
console.log(`build-documents: wrote ${items.length} document(s) → ${OUT}`);
